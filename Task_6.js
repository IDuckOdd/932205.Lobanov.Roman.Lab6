function left_column() {
    document.getElementById('pleft').className = 'pleft-left';
    document.getElementById('pright').className = 'pright-left';
}

function both_column() {
    document.getElementById('pleft').className = 'pleft-both';
    document.getElementById('pright').className = 'pright-both';
}

function right_column() {
    document.getElementById('pleft').className = 'pleft-right';
    document.getElementById('pright').className = 'pright-right';
}